//------------------------------------------------------------------------------
// Window interface
//------------------------------------------------------------------------------

#pragma once

#include "core.h"

//------------------------------------------------------------------------------
// Window data structure
//------------------------------------------------------------------------------

typedef struct {
    const char* title;
    int         width;
    int         height;
    int         scale;
    uint32_t*   buf;

#if OS_WINDOWS
    HWND hwnd;
#else
#    error "Unsupported OS"
#endif
} Window;

//------------------------------------------------------------------------------
// Window API
//------------------------------------------------------------------------------

void win_open(Window* w);
bool win_loop(Window* w);
